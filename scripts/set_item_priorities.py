def main() -> None:
    p = Path(__file__).parent.parent / "docs" / "progress" / "item.md"
    text = p.read_text(encoding="utf-8")
    lines = text.splitlines(keepends=True)

    result = []
    missing = []
    updated = 0
    for line in lines:
        parts = line.rstrip("\n\r").split("\t")
        if len(parts) >= 2 and parts[0] not in ("Name", "") and not parts[0].startswith("#"):
            name = parts[0]
            if name in PRIORITIES:
                if parts[1] != PRIORITIES[name]:
                    parts[1] = PRIORITIES[name]
                    updated += 1
                result.append("\t".join(parts) + "\n")
                continue
            else:
                missing.append(name)
        result.append(line)

    p.write_text("".join(result), encoding="utf-8")
    print(f"更新: {updated} 件")
    if missing:
        print("優先度未設定:")
        for m in missing:
            print(f"  {m}")


if __name__ == "__main__":
    main()
