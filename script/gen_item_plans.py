def spec_line(name: str) -> str:
    if name in HAS_SPEC:
        return f"- 仕様書: `spec/item/{name}.md`"
    return f"- 仕様書: 未作成（`spec/item/{name}.md` が未整備）"


def plan_implemented(name: str) -> str:
    test_line = "- テスト: ○ 名前一致あり" if name in HAS_TEST else "- テスト: 未実装"
    return f"""# {name} 実装計画

## スコープ
- 優先度: -
- {spec_line(name)[2:]}
- 実装状態: **実装済み**

## 実装済み内容
- `src/jpoke/data/item.py` の `{name}` に `handlers` を登録済み。
- `src/jpoke/handlers/item.py` に本体処理を実装済み。
- {test_line[2:]}

## 今後の対応
- 仕様書がない場合は `spec/item/{name}.md` を追加し、詳細仕様を記録する。
- テストが未実装の場合は `tests/test_item.py` に検証ケースを追加する。
- 仕様書の例外条件・相互作用が実装に反映されているか確認する。
"""


def plan_not_implemented(name: str) -> str:
    if name in HAS_SPEC:
        spec_task = f"`spec/item/{name}.md` の仕様を確認し、実装に反映するイベント候補を明示する。"
    else:
        spec_task = f"`spec/item/{name}.md` を追加し、効果/発動条件/無効化条件/解除条件を記述する。"
    return f"""# {name} 実装計画

## スコープ
- 優先度: -
- {spec_line(name)[2:]}
- 実装方針: 先に仕様書を作成し、仕様確定後にハンドラ実装する。

## 先行タスク
1. {spec_task}
2. 世代差分がある場合は対象バージョン（SV基準など）を明記する。
3. 実装に使うイベント候補を仕様書に明示する。

## 実装フロー（仕様確定後）
1. `src/jpoke/data/item.py` の `{name}` に `handlers` を登録する。
2. `src/jpoke/handlers/item.py` に本体処理を実装する。
3. `tests/test_item.py` に発動/非発動/解除/相互作用テストを追加する。

## テスト観点
- 発動条件を満たすケース。
- 発動条件を満たさないケース。
- 無効化または解除条件のケース。
- 交代・場状態・特性無効化との相互作用ケース。
"""


def main():
    out_dir = os.path.join(os.path.dirname(__file__), "..", "plan", "item")
    os.makedirs(out_dir, exist_ok=True)

    created = 0
    skipped = 0
    for name in ALL_ITEMS:
        path = os.path.join(out_dir, f"{name}.md")
        if os.path.exists(path):
            skipped += 1
            continue
        if name in IMPLEMENTED:
            content = plan_implemented(name)
        else:
            content = plan_not_implemented(name)
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        created += 1

    print(f"作成: {created} ファイル, スキップ（既存）: {skipped} ファイル")
    print(f"出力先: {os.path.abspath(out_dir)}")


if __name__ == "__main__":
    main()
